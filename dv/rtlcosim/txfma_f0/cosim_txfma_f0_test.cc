void step_and_compare(CosimCtrl<DUT>& sim) {
    sim.dut->opa_frf_clk = 0;
    sim.dut->opb_frf_clk = 0;
    sim.dut->opc_frf_clk = 0;
    sim.dut->trans_frf_clk = 0;
    sim.dut->ctrl_frf_clk = 0;
    sim.dut->eval();
    sim.sim_time++;
    sim.dut->opa_frf_clk = 1;
    sim.dut->opb_frf_clk = 1;
    sim.dut->opc_frf_clk = 1;
    sim.dut->trans_frf_clk = 1;
    sim.dut->ctrl_frf_clk = 1;
    sim.dut->eval();
    compare_outputs(sim);
    sim.sim_time++;
    sim.dut->opa_frf_clk = 0;
    sim.dut->opb_frf_clk = 0;
    sim.dut->opc_frf_clk = 0;
    sim.dut->trans_frf_clk = 0;
    sim.dut->ctrl_frf_clk = 0;
    sim.dut->eval();
    sim.sim_time++;
}

void check_coverage(CosimCtrl<DUT>& sim, const WordCoverage& coverage, const char* name) {
    sim.check(coverage.or_mask == coverage.mask, std::string(name) + " should drive every bit high");
    sim.check(coverage.and_mask == 0ull, std::string(name) + " should drive every bit low");
}

