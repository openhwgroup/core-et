#!/eng/dvtools/release/ruby/2.4.2/bin/ruby

# Hack the GEM_PATH To Karls Libary
ENV['GEM_PATH']="/eng/dvtools/release/ruby/mygems"
Gem.clear_paths

require 'json'
require "awesome_print"
require 'optparse'

def friendly_filename(filename)
    filename.gsub(/[^\w\s_-]+/, '')
            .gsub(/(^|\b\s)\s+($|\s?\b)/, '\\1\\2')
            .gsub(/\s+/, '_')
end


options = {}
OptionParser.new do |opts|
   opts.banner = "Usage: json_2_csr [options]"
   opts.on("-f filename", "--file filename", "name of file to convert") do |arg|
     options[:filename] = arg
   end
   opts.on("-o filename", "--output filename", "name of file to convert") do |arg|
     options[:out_filename] = arg
   end
end.parse!

unless options[:filename]
  puts "ERROR: -f filename is a required flag"
  exit
end

unless options[:out_filename]
  options[:out_filename]="#{File.basename(options[:filename],".*")}.csr"
end

data = JSON.parse(File.read options[:filename])

reg_name=""
reg_fields=[]
reg_offset=0x0

FILE_HEADER  = <<-EOS
// Generated by json_2_csr.rb
//

addressmap {
  property word_size = 4 bytes;
  property alignment_mode = "compact";
  property addressmap_bus_protocol = "basic";
EOS

f = File.new(options[:out_filename], File::CREAT|File::TRUNC|File::RDWR, 0644)
f.write FILE_HEADER

data['registers'].each do |reg_name, stuff|
  offset=stuff['offset']
  field_list=[]
  fields=stuff['fields'];
  fields_string=""
  fields.each do |key, prop|

    reset_value=prop['rstval']
    if prop['rstval'] == "SET"
      reset_value="1"
    elsif prop['rstval'] == "CLR"
      reset_value="0"
    elsif prop['rstval'] =~ /(\d)*{*(boot0_else1)}*/
      num_bits=$1.to_i
      if num_bits == 0
        reset_value="0"
      else
        reset_value="0"
      end
    end

    field_msb=prop['range'][0]
    field_lsb=prop['range'][1]

    if (field_lsb > field_msb) # Swap the order if lsb > msb
      temp=field_lsb
      field_lsb=field_msb
      field_msb=temp
    end
    field_width=field_msb-field_lsb+1

    field_template = <<-EOS
    field {
      property title = "#{key}";
      property description = "#{prop['comment']}";
      property width = #{field_width};
      property reset_value = #{reset_value};
      property output_port = true;
     } [#{field_msb}:#{field_lsb}] #{key};
EOS
     fields_string += field_template
   end

csr_reg_template = <<-EOS
  register {
    property title = "#{reg_name}";
    property description = "";
#{fields_string}
   } [#{stuff['offset']}] #{reg_name};
EOS

     f.write csr_reg_template
end

f.write "} #{friendly_filename(data['group'])};"
f.close
