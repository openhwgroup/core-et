void test_t1() {
  uint32_t data = 0;
  bool done = false;
  while(!done) {
    __asm__ __volatile__
      (
       "li t1,1\n"
       ".rept 64\n"
       "amoaddl.w t0, t1, (%[data])\n"
       ".endr 64\n"
       :
       : [data] "r" (&data)
       : "t0", "t1"
       );
    done = (peek_fcc(0) > 0);
  }
  
}

uint64_t peek_fcc(uint64_t cnt) {
  uint64_t fcc_nb;
  __asm__ __volatile__ ("csrr %[d], fccnb" : [d] "=r" (fcc_nb));
  return (fcc_nb >> (16*cnt)) &0xFFFF;
}

void inc_fcc0(unsigned thread){
  volatile uint64_t* trigger = ( thread % 2 == 0) ?
    (volatile uint64_t*) ESR_SHIRE(0xFF, FCC_CREDINC_0) :
    (volatile uint64_t*) ESR_SHIRE(0xFF, FCC_CREDINC_2);
  *trigger = 1ULL<<(thread>>1);
  __asm__ __volatile__ ("fence");
}

void set_flb(uint64_t cnt, uint64_t n){ 
  volatile uint64_t* flb = (volatile uint64_t*) ESR_SHIRE(0xFF, FAST_LOCAL_BARRIER0);
  flb[cnt] = n;
  __asm__ __volatile__("fence");
}

uint64_t get_flb(uint64_t cnt, uint64_t n) {
  uint64_t rd, rs;
  rs = ((n-1) << 5) | cnt;

  __asm__ __volatile__
    ( "csrrw %[rd], 0x820, %[rs]\n"
      : [rd] "=r" (rd)
      : [rs] "r"  (rs)
      );
  return rd;

}
uint64_t get_hart_id()
{
  uint64_t ret;
  __asm__ __volatile__
    (
     "csrr %[ret], mhartid\n"
     : [ret] "=r" (ret)
     );
  return ret;
}

void set_debug(uint64_t d){
  __asm__ __volatile__ ("csrw validation3, %[d]\n" : : [d] "r" (d) );
}

