/*******************************************************************************
 This test just tests the synchronization of the FCC/FCCNB between minions
 between the cosim/BEMU and the RTL
*******************************************************************************/

#define ITERATIONS 50
#define MAX_CREDIT_WAIT 32
#define CREDIT_READ_REPEAT 8
#define FCC_PER_THREAD 2 
uint8_t get_rand_byte(unsigned &ptr);

int main()
{

  C_TEST_START;
  
  uint64_t hart_id = getHartId();
  uint64_t shire_id  = ((hart_id>>6) & 0x3F);

  unsigned rand_ptr = hart_id;

  for ( int i = 0; i < ITERATIONS; i++) {
    unsigned other_thread = get_rand_byte(rand_ptr) % 64;
    unsigned other_counter = get_rand_byte(rand_ptr) % FCC_PER_THREAD;
    unsigned credits_to_give = get_rand_byte(rand_ptr) % 32;
    
    for (unsigned j = 0; j < credits_to_give; j++) {
      give_credit(shire_id, other_thread >> 1, other_thread & 1, other_counter);
      for (unsigned k = 0; k < CREDIT_READ_REPEAT; k++)
        for (unsigned c=0; c < FCC_PER_THREAD;c++)
          get_credit_count_nb(c);
    }

    unsigned counter =  get_rand_byte(rand_ptr) % FCC_PER_THREAD;
    unsigned available = get_credit_count_nb(counter);
    unsigned credits_to_wait = get_rand_byte(rand_ptr) % MAX_CREDIT_WAIT;
    if (credits_to_wait > available)  credits_to_wait = available;

    for (unsigned j = 0; j < available; j++) {
      wait_for_credit(counter);
      for (unsigned k = 0; k < CREDIT_READ_REPEAT; k++)
        for (unsigned c=0; c < FCC_PER_THREAD;c++)
          get_credit_count_nb(c);
    }


  }
    
  C_TEST_PASS;
  return 0;
}

