/*******************************************************************************
t0 uses FLB, t1 uses amos
trying to hit bug when FLB csr is accessed at the same time as an amo writes
back data
*******************************************************************************/

int main()
{

  C_TEST_START;

  __asm__ __volatile__
    (
     "la t0, mtrap_handler\n"
     "csrw mtvec, t0\n"
     : : : "t0");
  
  unsigned rand_ptr = 0;
  uint8_t masks[] = {0xFF, 0x55, 0xAA, 0};
  uint64_t debug;
  clear_fs();
  for ( unsigned m = 0; m < sizeof(masks); m++) {
    debug = masks[m] << 8;
    set_debug(debug);
    randomize_fregs(rand_ptr);

    debug |= 1;
    set_debug(debug);
    set_debug(masks[m]);
    set_mask(masks[m]);
    debug = (debug & ~0xF ) | 2;
    set_debug(debug);
    clear_fs();
    delay();
    
    // regular FP instructions
    __asm__ __volatile__ (".align 4\nfadd.ps f0, f1, f2\n");
    ILLEGAL_INST;
    clear_fs();

    debug = (debug & ~0xF ) | 3;
    set_debug(debug);
    delay();

    // trans

    __asm__ __volatile__ ("frcp.ps f0, f1\n");
    ILLEGAL_INST;
    clear_fs();

#ifdef FRSQ_IN_TRANS
    __asm__ __volatile__ ("frsq.ps f0, f1\n");
    ILLEGAL_INST;
    clear_fs();
#endif
    
    __asm__ __volatile__ ("flog.ps f0, f1\n");
    ILLEGAL_INST;
    clear_fs();

    __asm__ __volatile__ ("fexp.ps f0, f1\n");
    ILLEGAL_INST;
    clear_fs();

#ifdef FSIN_IN_TRANS
    __asm__ __volatile__ ("fsin.ps f0, f1\n");
    ILLEGAL_INST;
    clear_fs();
#endif
    debug = (debug & ~0xF ) | 4;
    set_debug(debug);
    delay();
    
    // loads with miss
    evict_l1();
    __asm__ __volatile__ ("flq2 f0, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(rand_ptr, 64)));
    ILLEGAL_INST;
    clear_fs();
    
    __asm__ __volatile__ ("flw.ps f0, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(rand_ptr, 64)));
    ILLEGAL_INST;
    clear_fs();

    
    debug = (debug & ~0xF ) | 5;
    set_debug(debug);
    delay();


    // loads with hit
    evict_l1();
    uint8_t *ptr =get_rand_byte_ptr(rand_ptr, 64);
    __asm__ __volatile__ ("flq2 f0, 0(%[m])\n"
                          "fence\n"
                          ".rept 4\n"
                          "nop\n"
                          ".endr\n"
                          : : [m] "r" (ptr));
    
    __asm__ __volatile__ ("flq2 f0, 0(%[m])\n" : : [m] "r" (ptr));
    ILLEGAL_INST;
    clear_fs();
    
    __asm__ __volatile__ ("flq2 f0, 0(%[m])\n"
                          "fence\n"
                          ".rept 4\n"
                          "nop\n"
                          ".endr\n"
                          : : [m] "r" (ptr));
    
    __asm__ __volatile__ ("flw.ps f0, 0(%[m])\n" : : [m] "r" (ptr));
    ILLEGAL_INST;
    clear_fs();

    
    debug = (debug & ~0xF ) | 6;
    set_debug(debug);
    delay();


  }

  
    
  C_TEST_PASS;
  return 0;
}

void evict_l1(){
#ifdef POSTSI_UST
    const uint64_t dest_level = 2;
#else
    const uint64_t dest_level = 3;
#endif
   for ( int way=0; way < 4; way ++){
    uint64_t evict =
      (dest_level << 58) | 
      (0<<14) | // starting at set 0
      (way<<6)|
      0xF;   // the whole 16 sets of the way
    __asm__ __volatile__ ( "csrw evict_sw, %[v]\n" : : [v] "r" (evict)  );
  }
   __asm__ __volatile__ ( "csrwi tensor_wait, 6\n");
}

void set_debug(uint64_t d){
  __asm__ __volatile__ ("csrw validation3, %[d]\n" : : [d] "r" (d) );
}

void set_mask(uint8_t m){
  __asm__ __volatile__ ("mova.m.x %[mask]\n": : [mask] "r" (m));
}

void randomize_fregs(unsigned &ptr){
  __asm__ __volatile__ ("flq2 f0, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(ptr)));
  __asm__ __volatile__ ("flq2 f1, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(ptr)));
  __asm__ __volatile__ ("flq2 f2, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(ptr)));
  __asm__ __volatile__ ("flq2 f3, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(ptr)));
  __asm__ __volatile__ ("flq2 f4, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(ptr)));
  __asm__ __volatile__ ("flq2 f5, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(ptr)));
  __asm__ __volatile__ ("flq2 f6, 0(%[m])\n" : : [m] "r" (get_rand_byte_ptr(ptr)));
}

void delay(){
  __asm__ __volatile__
    (
     ".rept 1024\n"
     "nop\n"
     ".endr"
     );

}

void mtrap_handler(){
  __asm__ __volatile__
    (
     "addi sp, sp, -8\n"
     "sd t0, 0(sp)\n"
     "csrr t0, mstatus\n"
     "ld t0, 0(sp)\n"
     "addi sp, sp, 8\n"
     "j mtrap_handler_func\n"
     );
}

void mtrap_handler_func(){
  __asm__ __volatile__ ("csrr t0, mstatus\n"  : : : "t0"); // mstatus.fs to be checked by checker
  
  uint64_t cause;
  __asm__ __volatile__ ("csrr %[cause], mcause\n"  : [cause] "=r" (cause));

  
  if (cause != ILLEGAL_CAUSE) { C_TEST_FAIL; }
  else {
    // set pc+=4, to skip illegal instruction
    __asm__ __volatile__
        (
         "csrr t0, mepc\n"
         "addi t0, t0, 4\n"
         "csrw mepc, t0\n"
         : : : "t0"
         );
  }
}

void clear_fs(){
  __asm__ __volatile__
    (
     "li t0, 1<<13\n"
     "csrs mstatus, t0\n"
     "li t0, 1<<14\n"
     "csrc mstatus, t0\n"
     ".rept 8\n"
     "nop\n"
     ".endr"
     : : : "t0"
     );
  

}

