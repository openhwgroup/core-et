uint8_t get_rand_byte(unsigned &ptr) {
  ptr%=sizeof(rand_bytes);
  return rand_bytes[ptr++];
}

uint8_t* get_rand_byte_ptr(unsigned &ptr, unsigned alignment) {
  uint8_t *ret;
  ptr%=sizeof(rand_bytes);
  ptr&= ~(alignment-1);
  ret = rand_bytes + ptr;
  ptr+=alignment;
  return ret;
}
