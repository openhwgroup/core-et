int main()
{

  C_TEST_START;
  unsigned rand_ptr = 0;
  uint64_t thread = get_hart_id() % 64;
  uint64_t cnt = 0;

  if (thread >=NR_THREADS) {
    C_TEST_PASS;
    return 0;
  }
  
  for ( int i = 0; i< ITERATIONS; i++) {
    // if thread > 0, wait for FCC.. thread0 will wake other threads
    if (thread > 0 ) {
      __asm__ __volatile__ ("csrw fcc, x0");
    } else {
      set_flb(cnt, 0);
      // and wake up other threads
      inc_fcc0(0xfffffffe, 0xffffffff);
    }
    

    
    // select a random target counter
    // each thread will read the FLB target*REPETITIONS times
    // so overall NR_THREADS*REPEPETITIONS*counter => 
    // the counter should be 0 at the end of each iterations,
    // regardless of the order the threads have accessed the counter
    
    
    uint64_t target = (get_rand_byte(rand_ptr) + 1) % MAX_FLB_CNT;
    unsigned total = target * REPETITIONS;
    uint64_t csr_conf;
    csr_conf = ((target-1) << 5) | cnt;


    set_debug( i | ( target << 16));


    // note: this is just a loop... manually unrolling to try to put csr ins as close as possible in time
    uint64_t loop_cnt = 0;
    while(total >= 16) {
      total-=16;
      loop_cnt++;
      __asm__ __volatile__
        (
         ".align 5\n"
         ".rept 2\n"
         "csrrw x10, 0x820, %[rs]\n"
         "csrrw x11, 0x820, %[rs]\n"
         "csrrw x12, 0x820, %[rs]\n"
         "csrrw x13, 0x820, %[rs]\n"
         "csrrw x14, 0x820, %[rs]\n"
         "csrrw x15, 0x820, %[rs]\n"
         "csrrw x16, 0x820, %[rs]\n"
         "csrrw x17, 0x820, %[rs]\n"
         ".endr\n"
         :
         : [rs] "r"  (csr_conf)
         : "x10","x11", "x12", "x13", "x14", "x15", "x16", "x17");


      // random write to unused counter
      if ( loop_cnt % 8 == thread % 8)
        set_flb((cnt+1) % NR_FLB, loop_cnt, false);

      // force stall with mulw=> so threads are not aligned
      for ( unsigned k = 0; k < (loop_cnt ^ thread) % 4; k++)
        __asm__ __volatile__("li t0, 1\n"
                             "mulw t0, t0, t0"
                             : : : "t0");
    }
    
#if 1
    uint64_t step = total % 16;
    uint64_t skip = (16 - step) * 4;
    __asm__ __volatile__
      (
       "la x10, 1f\n"
       "add x10, x10, %[skip]\n"
       "jalr x0, x10\n"
       "1:\n"
       ".rept 2\n"
       "csrrw x10, 0x820, %[rs]\n"
       "csrrw x11, 0x820, %[rs]\n"
       "csrrw x12, 0x820, %[rs]\n"
       "csrrw x13, 0x820, %[rs]\n"
       "csrrw x14, 0x820, %[rs]\n"
       "csrrw x15, 0x820, %[rs]\n"
       "csrrw x16, 0x820, %[rs]\n"
       "csrrw x17, 0x820, %[rs]\n"
       ".endr\n"
       "nop\n"
       :
       : [rs] "r"  (csr_conf), [skip] "r" (skip)
       : "x10","x11", "x12", "x13", "x14", "x15", "x16", "x17");
#else
    while(total-- > 0 ){
      __asm__ __volatile__("csrrw x0, 0x820, %[rs]\n"
                           : : [rs] "r"  (csr_conf) );
    }
#endif
    
    if (thread > 0 ) {
      inc_fcc0(1,0); // inc fcc for t0
    }else { 
      // wait other threads are done
      for ( int k = 0; k < NR_THREADS-1; k++) __asm__ __volatile__ ("csrw fcc, x0");

      // and check the counter is really 0
      set_debug( i | 0xFFFFFFFF00000000);
      volatile uint64_t* flb = (volatile uint64_t*) ESR_SHIRE(0xFF, FAST_LOCAL_BARRIER0);
      for ( uint64_t k = 0; k < REP_CHECKS; k++) {
        if (flb[cnt] != k ) { C_TEST_FAIL;}
        uint64_t res=get_flb(cnt, REP_CHECKS);
        if ( k != REP_CHECKS -1 && res != 0) { C_TEST_FAIL;}
        if ( k == REP_CHECKS -1 && res != 1) { C_TEST_FAIL;}
      }
    }
    
#ifndef ONLY_CNT_0
    cnt++;
    cnt%=NR_FLB;
#endif
    
    
  }
  
  
  C_TEST_PASS;
  return 0;
}


void inc_fcc0(unsigned thread0_mask, unsigned thread1_mask){
  volatile uint64_t* trigger0 = (volatile uint64_t*) ESR_SHIRE(0xFF, FCC_CREDINC_0);
  volatile uint64_t* trigger1 = (volatile uint64_t*) ESR_SHIRE(0xFF, FCC_CREDINC_2);
  *trigger0 = thread0_mask;
  *trigger1 = thread1_mask;
  __asm__ __volatile__ ("fence");
}

void set_flb(uint64_t cnt, uint64_t n, bool fence){ 
  volatile uint64_t* flb = (volatile uint64_t*) ESR_SHIRE(0xFF, FAST_LOCAL_BARRIER0);
  flb[cnt] = n;
  if (fence) __asm__ __volatile__("fence");
}

uint64_t get_flb(uint64_t cnt, uint64_t n) {
  uint64_t rd, rs;
  rs = ((n-1) << 5) | cnt;

  __asm__ __volatile__
    ( "csrrw %[rd], 0x820, %[rs]\n"
      : [rd] "=r" (rd)
      : [rs] "r"  (rs)
      );
  return rd;

}
uint64_t get_hart_id()
{
  uint64_t ret;
  __asm__ __volatile__
    (
     "csrr %[ret], mhartid\n"
     : [ret] "=r" (ret)
     );
  return ret;
}

void set_debug(uint64_t d){
  __asm__ __volatile__ ("csrw validation3, %[d]\n" : : [d] "r" (d) );
}

