module esr_spio
  # ( 
    //////////////////////////////////////////////////////////////////////    
    // CUSTOM PARAMS BEGIN 
    // CUSTOM PARAMS END
    //////////////////////////////////////////////////////////////////////    
      parameter AD_WIDTH = 4,
      parameter SETS_PER_SUB_BANK = `SC_SETS_PER_SUB_BANK,
      parameter NUM_REQQ_ENTRIES = `SC_REQQ_DEPTH
      )
   (
    //////////////////////////////////////////////////////////////
    // System signals
    //////////////////////////////////////////////////////////////
    input logic                         clock,
    input logic                         reset_c,
    input logic                         reset_w,
    input logic                         reset_d,
    input logic                         test_en,
    
    //////////////////////////////////////////////////////////////////////
    // BEGIN autogenerated PORTS - do not edit manually
   // SPIO ESRs
   output esr_spdmctrl_t esr_spdmctrl_op,

    // END autogenerated PORTS - do not edit manually   
    //////////////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////    
    // CUSTOM PORTS BEGIN 
    input  minion_debug_out_t    svcproc_dbg_status,
    output esr_sphastatus_t      esr_sphastatus_op,
    input  logic                 spio_rc_resumeack,
    // CUSTOM PORTS END
    //////////////////////////////////////////////////////////////////////    
    
    //////////////////////////////////////////////////////////////
    // APB slave interface
    //////////////////////////////////////////////////////////////
    // apb signals from master
    input [AD_WIDTH-1:0]                apb_paddr,
    input                               apb_pwrite, 
    input                               apb_psel, 
    input                               apb_penable,
    input [`ESR_APB_D_WIDTH-1:0]        apb_pwdata,
    
    // apb signals to master
    output logic                        apb_pready,
    output logic [`ESR_APB_D_WIDTH-1:0] apb_prdata,
    output logic                        apb_pslverr
    );
      
   ////////////////////////////////////////////////////////////////////////////////
   // SIGNAL declarations
   ////////////////////////////////////////////////////////////////////////////////
   
   // for APB slave
   logic                                       s0_en;
   logic                                       s0_addr_valid;
   logic [`ESR_APB_D_WIDTH-1:0]                s0_rdata;
   
   logic                                       s1_en;
   logic                                       s1_addr_valid;
   logic [`ESR_APB_D_WIDTH-1:0]                s1_rdata;
   logic [`ESR_APB_D_WIDTH-1:0]                s1_wdata;
   // CUSTOM SIGNALS BEGIN 
   esr_sphastatus_t     read_sphastatus;
   esr_spdmctrl_t       read_spdmctrl;
   // CUSTOM SIGNALS END
   
   ////////////////////////////////////////////////////////////////////////////////
   // include autogenerated code from esr scripts
   ////////////////////////////////////////////////////////////////////////////////
`include "esr_spio_auto.v"

   ////////////////////////////////////////////////////////////////////////////////
   // APB slave implementation
   ////////////////////////////////////////////////////////////////////////////////

   // s0: setup phase (address decoding + read)
   always_comb begin
      s0_en = apb_psel && ! apb_penable;
      s0_addr_valid = 1'b1;
      s0_rdata = '0;

      // set enables to 0 by default
      esr_read_and_decode_comb_default();
      
      // decode
      `esr_read_and_decode_comb_cases_spio

      // CUSTOM POST_DECODE BEGIN
      // CUSTOM POST_DECODE END
   end

   // s1: access phase
   always_comb begin
      s1_en = apb_psel && apb_penable;
      
      // by default, keep the same value
      `esr_write_comb_default_spio

      // update regs on write
      `esr_write_comb_spio

      // CUSTOM POST_WRITE_COMB BEGIN
      reg_spdmctrl_next.reserved0   = '0;
      reg_spdmctrl_next.reserved1   = '0;
      reg_spdmctrl_next.reserved2   = '0;

      if (reg_spdmctrl_en == 1'b0) begin
        reg_spdmctrl_next.clrresethaltreq = 1'b0; // Pulse field
        reg_spdmctrl_next.setresethaltreq = 1'b0; // Pulse field
        reg_spdmctrl_next.ackhavereset    = 1'b0; // Pulse field
        reg_spdmctrl_next.resumereq    = 1'b0; // Pulse field
      end

      reg_sphastatus_next.error     = ( svcproc_dbg_status.error[0] ) ? 1'b1 : // SvcProc asserts error bit
                                      ( s1_en & !apb_pwdata[6] & (apb_paddr == 4'hB)) ? 1'b0 : // APB clears error bit
                                      reg_sphastatus.error;

      reg_sphastatus_next.exception = ( svcproc_dbg_status.exception[0] ) ? 1'b1 : // SvcProc asserts exception bit
                                       ( s1_en & !apb_pwdata[5] & (apb_paddr == 4'hB)) ? 1'b0 : // APB clears exception bit
                                       reg_sphastatus.exception;

      reg_sphastatus_next.havereset  = svcproc_dbg_status.have_reset[0];

      reg_sphastatus_next.busy       = svcproc_dbg_status.busy[0];
      reg_sphastatus_next.resumeack  = spio_rc_resumeack;
      reg_sphastatus_next.running    = svcproc_dbg_status.running[0];
      reg_sphastatus_next.halted     = svcproc_dbg_status.halted[0];  
    
      // CUSTOM POST_WRITE_COMB END

   end

   `EN_FF(clock, s0_en &&  apb_pwrite, s1_wdata, apb_pwdata)
   `EN_FF(clock, s0_en && !apb_pwrite, s1_rdata, s0_rdata)
   `RST_EN_FF(clock, reset_c, s0_en, s1_addr_valid, s0_addr_valid, 1'b0)
   
   // assign APB slave outputs
   assign apb_prdata = s1_rdata;
   assign apb_pslverr = !s1_addr_valid;
   assign apb_pready = 1'b1;


   //////////////////////////////////////////////////////////////////////
   // CUSTOM STATEMENTS BEGIN 
   always_comb begin
      read_sphastatus            = reg_sphastatus;

      read_sphastatus.reserved   = '0;
      read_sphastatus.busy       = svcproc_dbg_status.busy[0];

      read_sphastatus.havereset  = svcproc_dbg_status.have_reset[0];

      read_sphastatus.resumeack  = spio_rc_resumeack;

      read_sphastatus.running    = svcproc_dbg_status.running[0];
      read_sphastatus.halted     = svcproc_dbg_status.halted[0];
      esr_sphastatus_op          = read_sphastatus;
   end

   always_comb begin
      read_spdmctrl                  = reg_spdmctrl;
      read_spdmctrl.clrresethaltreq = '0;
      read_spdmctrl.setresethaltreq = '0;
      read_spdmctrl.reserved0       = '0;
      read_spdmctrl.reserved1       = '0;
      read_spdmctrl.reserved2       = '0;
      read_spdmctrl.ackhavereset    = '0;
      read_spdmctrl.resumereq       = '0;
      read_spdmctrl.haltreq         = '0;
   end
   // CUSTOM STATEMENTS END 
   //////////////////////////////////////////////////////////////////////
endmodule
